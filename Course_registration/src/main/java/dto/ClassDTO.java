package dto;

public class ClassDTO {
	private int classidx, classunit;
	private String classname, classpro, makelesson, classdivision, classtime;
	
	public int getClassidx() {
		return classidx;
	}
	public void setClassidx(int classidx) {
		this.classidx = classidx;
	}
	public int getClassunit() {
		return classunit;
	}
	public void setClassunit(int classunit) {
		this.classunit = classunit;
	}
	public String getClassname() {
		return classname;
	}
	public void setClassname(String classname) {
		this.classname = classname;
	}
	public String getClasspro() {
		return classpro;
	}
	public void setClasspro(String classpro) {
		this.classpro = classpro;
	}
	public String getMakelesson() {
		return makelesson;
	}
	public void setMakelesson(String makelesson) {
		this.makelesson = makelesson;
	}
	public String getClassdivision() {
		return classdivision;
	}
	public void setClassdivision(String classdivision) {
		this.classdivision = classdivision;
	}
	public String getClasstime() {
		return classtime;
	}
	public void setClasstime(String classtime) {
		this.classtime = classtime;
	}
	
	
}
